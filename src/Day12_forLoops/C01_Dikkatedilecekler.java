package Day12_forLoops;

public class C01_Dikkatedilecekler {

    public static void main(String[] args) {

        for (int i =200; i < 10; i++) {

            System.out.print(i + " ");

        }
        for (int i = 0; i <10 ; i--) {
            System.out.print(i + " ");

            //loop basladiktan sonra/azalis yontemi sebebiyle

        }
    }
}
